package com.innova.assignment.model;

public class Rectangle {
    private Integer firstXPoint;
    private Integer firstYPoint;
    private Integer secondXPoint;
    private Integer secondYPoint;

    public Integer getFirstXPoint() {
        return firstXPoint;
    }

    public void setFirstXPoint(Integer firstXPoint) {
        this.firstXPoint = firstXPoint;
    }

    public Integer getFirstYPoint() {
        return firstYPoint;
    }

    public void setFirstYPoint(Integer firstYPoint) {
        this.firstYPoint = firstYPoint;
    }

    public Integer getSecondXPoint() {
        return secondXPoint;
    }

    public void setSecondXPoint(Integer secondXPoint) {
        this.secondXPoint = secondXPoint;
    }

    public Integer getSecondYPoint() {
        return secondYPoint;
    }

    public void setSecondYPoint(Integer secondYPoint) {
        this.secondYPoint = secondYPoint;
    }
}
